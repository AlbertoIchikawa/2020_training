class Manual:
    def __init__(self):
        self.number_seats = 0
        self.engine = None
        self.trip = None
        self.gps = None

    def get_seats(self):
        return self.number_seats

    def set_engine(self):
        return self.engine

    def set_trip_computer(self):
        return self.trip

    def set_gps(self):
        return self.gps
